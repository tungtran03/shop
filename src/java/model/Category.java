package model;

/**
 *
 * @author tung
 */
public class Category {
     private int id;
    private String tenloai;

    public Category(int id, String tenloai) {
        this.id = id;
        this.tenloai = tenloai;
    }

    public Category() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTenloai() {
        return tenloai;
    }

    public void setTenloai(String tenloai) {
        this.tenloai = tenloai;
    }
    
    
}
